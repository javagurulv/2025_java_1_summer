package org.javaguru.mentor.lesson_2_variables_and_data_types.lessoncode;

public class SuperFloatCalculator {

    public static void main(String[] args) {

        float parameter1 = 10.7f;
        float parameter2 = 5.4f;

        float sum = parameter1 + parameter2;
        float sub = parameter1 - parameter2;
        float mult = parameter1 * parameter2;
        float devid = parameter1 / parameter2;

        System.out.println("Sum = " + sum);
        System.out.println("Sub = " + sub);
        System.out.println("Multiply = " + mult);
        System.out.println("Divide = " + devid);
    }


}
