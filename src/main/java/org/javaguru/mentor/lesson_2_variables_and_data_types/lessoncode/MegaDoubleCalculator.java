package org.javaguru.mentor.lesson_2_variables_and_data_types.lessoncode;

public class MegaDoubleCalculator {

    public static void main(String[] args) {

        double parameter1 = 10.7;
        double parameter2 = 5.4;

        double sum = parameter1 + parameter2;
        double sub = parameter1 - parameter2;
        double mult = parameter1 * parameter2;
        double devid = parameter1 / parameter2;

        System.out.println("Sum = " + sum);
        System.out.println("Sub = " + sub);
        System.out.println("Multiply = " + mult);
        System.out.println("Divide = " + devid);
    }


}
